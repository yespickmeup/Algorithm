    private void ok1() {
        double amount = FitIn.toDouble(tf_amount.getText());
        if (amount < 0) {
            Alert.set(0, "ENTER AMOUNT");
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(amount));
        }
    }

    private void closed() {
        if (callback != null) {
            callback.close(new CloseDialog(this), new OutputData(0));
        }
    }
