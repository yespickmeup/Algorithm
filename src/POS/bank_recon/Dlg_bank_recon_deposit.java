    private void ok() {
        String reference_no = jTextField9.getText();
        double cash = FitIn.toDouble(jTextField4.getText());
        double check_amount = FitIn.toDouble(jTextField5.getText());
        if (cash <= 0 && check_amount <= 0) {
            Alert.set(0, "Input Amount");
            jTextField4.grabFocus();
            return;
        }
        String check_holder = jTextField6.getText();
        String check_no = jTextField7.getText();
        String check_date = DateType.sf.format(jDateChooser1.getDate());
        int is_check_cleared = 0;
        if (jCheckBox1.isSelected()) {
            is_check_cleared = 1;
        }
        String remarks = jTextField8.getText();

        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(reference_no, cash, check_amount, check_holder, check_no, check_date, is_check_cleared, remarks));
        }
    }
