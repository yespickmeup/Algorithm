    private void create() {
        String path = System.getProperty("user.home");
        JFileChooser f = new JFileChooser(path + "/");
        f.setDialogTitle("Save Ending Inventory");
        f.setMultiSelectionEnabled(false);
        f.setFileFilter(new FileNameExtensionFilter("Test", "secret"));
        f.setApproveButtonText("Save");
        f.setApproveButtonMnemonic('S');
        f.setAcceptAllFileFilterUsed(false);

        int action = f.showDialog(this, "Save");
        if (action == JFileChooser.APPROVE_OPTION) {
            File file = f.getSelectedFile();

            if (file.getName().
                    isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please choose a filename");
            }

            try {

                int id = 0;
                String transaction_no = "123445";
                String user_name = "user";
                String date_added = "";
                String remarks = "";
                String to_branch = "";
                String to_branch_id = "";
                String to_location = "";
                String to_location_id = "";
                String from_branch = "";
                String from_branch_id = "";
                String from_location = "";
                String from_location_id = "";
                int status = 1;

                SaveToUsb usb = new SaveToUsb();
                SaveToUsb.stock_transfer stock = new SaveToUsb.stock_transfer(id, transaction_no, user_name, date_added, remarks, to_branch, to_branch_id, to_location, to_location_id, from_branch, from_branch_id, from_location, from_location_id, status);

                usb.setStock_transfer(stock);
                SaveToUsb.save(file, usb);

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }

    private void open() {

        String path = System.getProperty("user.home");
        JFileChooser f = new JFileChooser(path + "/");
        f.setDialogTitle("Open To Ending Inventory");
        f.setFileFilter(new FileNameExtensionFilter("Test", "secret"));
        f.setAcceptAllFileFilterUsed(false);
        f.setControlButtonsAreShown(true);
        f.setMultiSelectionEnabled(false);
        int action = f.showDialog(this, "Open");
        if (action == JFileChooser.APPROVE_OPTION) {
            File file = f.getSelectedFile();
            if (file.getName().
                    isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please choose a filename");
            }
            try {
                SaveToUsb to = (SaveToUsb) SaveToUsb.get(file);
                jLabel2.setText(to.stock_transfer.transaction_no);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }

