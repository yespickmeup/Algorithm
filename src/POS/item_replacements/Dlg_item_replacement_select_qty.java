    private void ok() {
        double qty = FitIn.toDouble(jTextField8.getText());
        if (qty <= 0) {
            Alert.set(0, "Enter Quantity!");
            jTextField8.grabFocus();
            return;
        }
        String serial = jTextArea2.getText();
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, serial));
        }
    }
