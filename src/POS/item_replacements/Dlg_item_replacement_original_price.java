    public void do_pass(MySales_Items.items item) {
        jTextField3.setText(item.item_code);
        jTextArea1.setText(item.description);
        jTextField4.setText(FitIn.fmt_wc_0(item.selling_price));

        List<Inventory_barcodes.to_inventory_barcodes> datas = Inventory_barcodes.ret_where(" where main_barcode='" + item.item_code + "' and location_id='" + item.location_id + "' ");
        if(!datas.isEmpty()){
            Inventory_barcodes.to_inventory_barcodes to=(Inventory_barcodes.to_inventory_barcodes) datas.get(0);
            jTextField5.setText(FitIn.fmt_wc_0(to.selling_price));
        }
    }

