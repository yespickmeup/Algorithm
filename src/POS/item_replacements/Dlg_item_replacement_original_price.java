    private void ok() {
        double price=FitIn.toDouble(jTextField4.getText());
        if(!jCheckBox1.isSelected()){
            price=FitIn.toDouble(jTextField5.getText());
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(price));
        }
    }
