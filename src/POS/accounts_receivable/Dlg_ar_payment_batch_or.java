    private void ok() {

        String or = tf_customer_id.getText();
        if (or.isEmpty()) {
            Alert.set(0, "Input field");
            tf_customer_id.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(or));
        }
    }
