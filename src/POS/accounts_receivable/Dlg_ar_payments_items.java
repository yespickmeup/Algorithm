    public void do_pass(S1_accounts_receivable.to_accounts_receivable to) {
        jTextField4.setText(to.ar_no);

        String where = " where charge_reference_no='" + to.reference_no + "' and charge_customer_id='" + to.customer_id + "' ";
        List<MySales.sales> sales = MySales.ret_data(where);
        if (!sales.isEmpty()) {
            MySales.sales sale = (MySales.sales) sales.get(0);
            jTextField5.setText(sale.sales_no);
            jTextField8.setText(FitIn.fmt_wc_0(sale.charge_amount));
            jTextField6.setText(sale.charge_customer_id);
            jTextField7.setText(sale.charge_customer_name);
            String where2 = " where sales_no='" + sale.sales_no + "' ";
            List<MySales_Items.items> items = MySales_Items.ret_data2(where2);
            loadData_sale_items(items);
            jLabel7.setText("" + items.size());
        }

    }

