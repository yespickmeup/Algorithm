    private void get_data() {
        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        Thread t = new Thread(new Runnable() {

            @Override
            public void run() {
                String date_from = DateType.sf.format(dp_from.getDate());
                String date_to = DateType.sf.format(dp_to.getDate());
                String supplier = "";
                String where = "";
                if (jCheckBox1.isSelected()) {
                    where = " where date(date_added) between '" + date_from + "' and '" + date_to + "' order by supllier_id,description asc";
                } else {
                    where = " where date(date_added) between '" + date_from + "' and '" + date_to + "' ";
                    where = where + " and supllier_id='" + tf_supplier_id.getText() + "' order by supllier_id,description asc ";
                }
                List<Srpt_receipts_items_by_supplier.field> datas = Srpt_receipts_items_by_supplier.ret_data(where);
                String business_name = System.getProperty("business_name", "Algorithm Computer Services");
                String date = DateType.month_date.format(new Date());
                String printed_by = "";//Users.screen_name.toUpperCase();
                Srpt_receipts_items_by_supplier rpt = new Srpt_receipts_items_by_supplier(business_name, date, printed_by);
                rpt.fields.addAll(datas);
                String jrxml = "rpt_receipt_items_by_supplier.jrxml";

                report_sales_items(rpt, jrxml);
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();

    }

    private void report_sales_items(final Srpt_receipts_items_by_supplier to, String jrxml_name) {
        pnl_sales_items.removeAll();
        pnl_sales_items.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_all_sales(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_sales_items.add(viewer);
            pnl_sales_items.updateUI();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_all_sales(Srpt_receipts_items_by_supplier to, String rpt_name) {
//        Connection con11 = null;
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.makeDatasource(to.fields));
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Srpt_receipts_items_by_supplier.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }
