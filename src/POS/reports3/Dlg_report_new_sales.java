    private void get_stocks() {
        jProgressBar1.setString("Loading. Please wait....");
        jProgressBar1.setIndeterminate(true);
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                String date_from = DateType.slash.format(jDateChooser1.getDate());
                String date_from2 = DateType.sf.format(jDateChooser1.getDate());
                String date_to = DateType.slash.format(jDateChooser2.getDate());
                String date_to2 = DateType.sf.format(jDateChooser2.getDate());
                String sales_date = "Sales Ledger Report as of [" + date_from + "] - [" + date_to + "]";
                Srpt_sales_ledger rpt = new Srpt_sales_ledger(sales_date);
                String where = " where Date(date_added) between '" + date_from2 + "' and '" + date_to2 + "' ";
                if (jCheckBox1.isSelected()) {
                    where = where + " and discount_customer_name like '%" + jTextField2.getText() + "%' ";
                } else {
                    where = where + " and sales_no like '%" + jTextField2.getText() + "%' ";
                }

                if (!jCheckBox5.isSelected()) {
                    if (jCheckBox4.isSelected()) {
                        where = where + " and sales_type ='" + "0" + "' ";
                    } else {
                        where = where + " and sales_type  ='" + "1" + "' ";
                    }
                }
                if (!jTextField3.getText().isEmpty()) {
                    where = where + " and user_name  like '" + jTextField3.getText() + "' ";
                }
                List<Srpt_sales_ledger.field> fields = Srpt_sales_ledger.ret_data(where);
                rpt.fields.addAll(fields);
                final String jrxml = "rpt_sales_ledger.jrxml";
                report_sales_items(rpt, jrxml);
                jProgressBar1.setString("Finished!");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();
    }

