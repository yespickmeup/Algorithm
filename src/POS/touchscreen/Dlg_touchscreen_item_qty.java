    private void type() {
        double qty = FitIn.toDouble(tf_qty.getText());
        if (qty < 0) {
            tf_qty.setText("1");
        }
    }

    private void ok() {
        double qty = FitIn.toDouble(tf_qty.getText());
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty));
        }
    }
