    private void ok() {
        if (callback != null) {
            int row = tbl_inventory.getSelectedRow();
            if (row < 0) {
                return;
            }
            to_inventory to = (to_inventory) tbl_inventory_ALM.get(row);

            callback.ok(new CloseDialog(this), new OutputData(to.barcode));
        }
    }
