    private void ok() {
        String from = tf_from.getText();
        String description = tf_description.getText();
        String gc_no = tf_gc_no.getText();
        double amount = FitIn.toDouble(tf_amount.getText());
        Payments.gift_certificate gc = new Payments.gift_certificate(from, description, gc_no, amount);
        Payments.gift_certificate = gc;
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData());
        }
    }
