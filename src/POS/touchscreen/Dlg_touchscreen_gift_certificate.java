    public void do_pass() {
        Payments.gift_certificate gc = Payments.gift_certificate;
        if (gc.amount == 0) {
            tf_amount.setText(FitIn.fmt_wc_0(Payments.countChange()));
        } else {
            tf_from.setText(gc.from);
            tf_description.setText(gc.from);
            tf_gc_no.setText(gc.from);
            tf_amount.setText(FitIn.fmt_wc_0(gc.amount));
        }
    }

