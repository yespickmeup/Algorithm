    private void ok() {
        double qty = FitIn.toDouble(jTextField1.getText());
        String serial_no = jTextArea1.getText();
        if (qty <= 0) {
            Alert.set(0, "Enter Quantity");
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, serial_no));
        }
    }

