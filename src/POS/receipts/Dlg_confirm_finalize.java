    private void ok() {

        int is_invoice = 0;
        if (jCheckBox2.isSelected()) {
            is_invoice = 1;
        }
        int is_payable = 0;
        if (jCheckBox3.isSelected()) {
            is_payable = 1;
        }
        String ap_date = DateType.sf.format(jDateChooser1.getDate());
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(is_invoice, is_payable, ap_date));
        }
    }
