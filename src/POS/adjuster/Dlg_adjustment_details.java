    public void do_pass(S1_adjustments.to_adjustments to) {
        jTextField1.setText("" + to.id);
        jTextField2.setText(DateType.convert_slash_datetime2(to.date_added));
        jTextField3.setText(to.user_screen_name);
        jTextField4.setText(to.item_code);
        jTextField5.setText(to.barcode);
        jTextField6.setText(to.description);
        jTextArea1.setText(to.remarks);
        jTextField7.setText(FitIn.fmt_woc(to.qty));
        jTextField8.setText(FitIn.fmt_woc(to.new_qty));

        if (to.is_add == 1) {
            jTextField9.setText(FitIn.fmt_woc(to.new_qty + to.qty));
            jCheckBox1.setSelected(true);
        } else {
            jTextField9.setText(FitIn.fmt_woc(to.qty - to.new_qty));
            jCheckBox2.setSelected(true);
        }
    }

