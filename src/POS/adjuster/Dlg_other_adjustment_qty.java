    public void do_pass(String branch, String location, String item_code, String description, int type, double qty, Date d, double item_qty) {
        jTextField3.setText(branch);
        jTextField4.setText(location);
        jTextField1.setText(item_code);
        jTextArea1.setText(description);
        if (type==0) {
            jCheckBox1.setSelected(true);
        }
        if (type==1) {
            jCheckBox2.setSelected(true);
        }
        if (type==2) {
            jCheckBox3.setSelected(true);
        }
        if (type==3) {
            jCheckBox4.setSelected(true);
        }
        if (type==4) {
            jCheckBox5.setSelected(true);
        }
        jTextArea1.setText(description);
        jTextField2.setText(FitIn.fmt_woc(qty));
        jTextField5.setText(FitIn.fmt_woc(item_qty));
        jDateChooser1.setDate(d);
        jTextField2.grabFocus();
    }

