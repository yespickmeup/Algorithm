    private void ok() {
        int type = 0;// "Damage";
        if (jCheckBox2.isSelected()) {
            type = 1;//"Leftover";
        }
        if (jCheckBox3.isSelected()) {
            type = 2;//"Voucher";
        }
        if (jCheckBox4.isSelected()) {
            type = 3;//"Short";
        }
        if (jCheckBox5.isSelected()) {
            type = 4;//"Over";
        }

        double qty = FitIn.toDouble(jTextField2.getText());
        if (qty < 1) {
            Alert.set(0, "Input quantity!");
            jTextField2.grabFocus();
            return;
        }
        String date = DateType.sf.format(jDateChooser1.getDate());
        date = date + " " + DateType.time4.format(new Date());
        String remarks = jTextArea2.getText();
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(type, qty, date, remarks));
        }
    }
