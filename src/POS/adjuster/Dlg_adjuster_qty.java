    private void ok() {
        double qty = FitIn.toDouble(jTextField1.getText());
        String remarks = jTextArea1.getText();
        if (jTextArea1.getText().isEmpty()) {
            Alert.set(0, "Please Enter Reason");
            return;
        }
        int is_add = 1;
        if (jCheckBox2.isSelected()) {
            is_add = 0;
        }
        String date=DateType.datetime.format(jDateChooser1.getDate());
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, is_add, remarks,date));
        }
    }
