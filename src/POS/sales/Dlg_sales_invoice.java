    private void set_report() {

        jProgressBar1.setString("Loading...Please wait...");
        jProgressBar1.setIndeterminate(true);
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {

                List<Srpt_sales_invoice.field> fields = new ArrayList();

                String sold_to = "";
                String date = "";
                String address = "";
                String tin = "";
                String business_style = "";
                String terms = "";
                String id_no = "";
                String pwd_sig = "";
                double total_sales = 2200;
                double discount = 0;
                double vatable_sales = 1964;
                double vatable_amount = 235.71;
                double total_amount_due = 2200;
                for (int i = 0; i < 10; i++) {
                    String qty = "" + i;
                    String unit = "PC";
                    String articles = " (6833) KVR 1333/4GB";
                    String unit_price = "";
                    String discount1 = "";
                    String amount = "2200.00";
                    Srpt_sales_invoice.field to = new Srpt_sales_invoice.field(qty, unit, articles, unit_price, discount1, amount);
                    fields.add(to);

                }
                Srpt_sales_invoice rpt = new Srpt_sales_invoice(sold_to, date, address, tin, business_style, terms, id_no, pwd_sig, total_sales, discount, vatable_sales, vatable_amount, total_amount_due);
                rpt.fields.addAll(fields);
                String jrxml = "rpt_sales_invoice.jrxml";
                report_customers(rpt, jrxml);
                jProgressBar1.setString("Finished...");
                jProgressBar1.setIndeterminate(false);
            }
        });
        t.start();
    }

    private void report_customers(final Srpt_sales_invoice to, String jrxml_name) {
        pnl_sales_items.removeAll();
        pnl_sales_items.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_customers(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_sales_items.add(viewer);
            pnl_sales_items.updateUI();
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_customers(Srpt_sales_invoice to, String rpt_name) {
        try {
            return JasperUtil.getJasperViewer(
                    compileJasper(rpt_name),
                    JasperUtil.setParameter(to),
                    JasperUtil.makeDatasource(to.fields));
        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Srpt_sales_invoice.class.
                    getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }

